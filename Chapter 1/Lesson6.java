public class Lesson6 {
    public static void main(String[] args) {
        int myNum = 10;
        int talk = myNum + 1;
        myNum = 14;
        System.out.println(myNum);

        float number = 5.99f;
        System.out.println(number);

        final int geekeed = 15; // This cannot be changed unlike above
        System.out.println(talk+geekeed);
    }
}

