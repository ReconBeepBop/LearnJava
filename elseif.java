public class elseif {
    public static void main(String[] args) {
        int weather = 2;

        if (weather == 1) {
            System.out.println("Bring Umbrella.");
        } else if (weather == 2) {
            System.out.println("Wear sunglasses.");
        } else {
            System.out.println("Just go out normally .");
        }
    }
}
