public class Do_While {
    public static void main(String[] args) {
        int i = 0;
        do { 
            System.out.println(i);
            i++;
        } while (i<5);

        int t = 10;

        do {
            System.out.println("i is " + t);
            i++;
        } while (i < 5);
    }
}
